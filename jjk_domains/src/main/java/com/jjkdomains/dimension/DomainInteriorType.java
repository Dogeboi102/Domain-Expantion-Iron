package com.jjkdomains.dimension;

/**
 * Identifies which domain interior style to build.
 * Each value corresponds to a unique block layout inside the domain sphere.
 */
public enum DomainInteriorType {
    /** Infinite Void — deep purple-black void with floating obsidian fragments and end-like wisps */
    INFINITE_VOID,

    /** Malevolent Shrine — crimson Nether-flesh walls, bone pillars, lava channels */
    MALEVOLENT_SHRINE,

    /** Deadly Sentencing — sandstone courtroom with lecterns, banners, and iron bars */
    DEADLY_SENTENCING,

    /** Coffin of the Iron Mountain — blackstone cavern, magma blocks, enclosed crushing ceiling */
    COFFIN_OF_IRON_MOUNTAIN
}
